public class Student {
    /**
     * s_i 4 student id
     * s_cN 4 student class number
     * s_s 4 student school
     * s_c 4 student class
     * s_n 4 student name
     */
    String s_i,s_cN,s_s,s_c,s_n;

    public String getS_i() {
        return s_i;
    }

    public void setS_i(String s_i) {
        this.s_i = s_i;
    }

    public String getS_cN() {
        return s_cN;
    }

    public void setS_cN(String s_cN) {
        this.s_cN = s_cN;
    }

    public String getS_s() {
        return s_s;
    }

    public void setS_s(String s_s) {
        this.s_s = s_s;
    }

    public String getS_c() {
        return s_c;
    }

    public void setS_c(String s_c) {
        this.s_c = s_c;
    }

    public String getS_n() {
        return s_n;
    }

    public void setS_n(String s_n) {
        this.s_n = s_n;
    }
}
